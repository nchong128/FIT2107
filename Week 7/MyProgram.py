# This method calculates the square of the number
def square(num):
    return num ** 2

# This method calculates the cube of the number
def cube(num):
    return num **3
    
# main method to print the results
def main():
    print("2 square is: ",square(3))
    print("2.19 square is: ",square(2.19))
    print("2 cube is: ",cube(2))
    print("2.19 cube is: ",cube(2))

main()